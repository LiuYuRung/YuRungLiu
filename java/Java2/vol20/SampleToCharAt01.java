// str.charAt(0) = X
// str.charAt(1) = Y
// str.charAt(2) = Z
class SampleToCharAt01{
    public static void main(String[] args){

        String str = "XYZ";

        System.out.println("str.charAt(0) = " + str.charAt(0));
        System.out.println("str.charAt(1) = " + str.charAt(1));
        System.out.println("str.charAt(2) = " + str.charAt(2));
    }
}