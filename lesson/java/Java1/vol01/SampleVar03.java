class SampleVar03 {
    public static void main(String[] args){
        byte b1 = 127;
        byte b2 = -128;
        // byte b3 =128;

        short s1 = 32767;
        short s2 = -32768;
        // short s3 = 32768;

        int i1 = 2147483647;
        int i2 = -2147483648;
        // int i3 = 2147483648;

        System.out.println(b1);
        System.out.println(b2);
        System.out.println(s1);
        System.out.println(s2);
        System.out.println(i1);
        System.out.println(i2);
    }
}