class Rel01{
    public static void main(String[] args){
        System.out.println("5 < 8 -> " + (5 < 8));
        System.out.println("5 <= 5 -> " + (5<= 5));
        System.out.println("5 > 8 -> " + (5 > 8));
        System.out.println("0 >= 0 -> " + (0 >= 0));
        System.out.println("5 == 5 -> " + (5 == 5));
        System.out.println("5 != 8 ->" + (5 != 8));
    }
}