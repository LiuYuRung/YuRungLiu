class SampleArray05{
    public static void main(String[] args){
        int[] ai = new int[5]; //int型配列の宣言

        // 各要素に値を代入
        ai[0] = 34;
        ai[1] = 58;
        ai[2] = 7;
        ai[3] = 89;
        ai[4] = 100;

        //各要素を表示
        System.out.println("ai[0] =" + ai[0]);
        System.out.println("ai[1] =" + ai[1]);
        System.out.println("ai[2] =" + ai[2]);
        System.out.println("ai[3] =" + ai[3]);
        System.out.println("ai[4] =" + ai[4]);
    }
}